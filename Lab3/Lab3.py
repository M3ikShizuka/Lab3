def main():
    text = input("Text: ")
    count = {}

    # Количество повторений символов.
    for char in text:
        if char.isalpha() == False:
            continue
        char = char.lower()
        if char in count:
            count[char] += 1
        else:
            count[char] = 1

    # Получить ключи
    dict_keys = count.keys()
    # Преобразовать словарь в список
    list_keys = list(dict_keys)
    # Сортировка списка
    list_keys.sort()
 
    # Вывод
    for key in list_keys:
        print("%s%i" % (key, count[key]))
#####################################################################################################################
##################################################### Exec: #########################################################
#####################################################################################################################
main()